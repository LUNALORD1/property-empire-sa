export type PropertyTier = 1 | 2 | 3 | 4 | 5;

function hashString(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) {
    h = (h * 31 + s.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

function suburbCharSum(s: string): number {
  let sum = 0;
  for (let i = 0; i < s.length; i++) sum += s.charCodeAt(i);
  return sum;
}

/** Returns the deterministic Unsplash photo ID for a property. */
export function pickPropertyPhotoId(
  tier: PropertyTier,
  propertyId: string | null | undefined,
  suburb: string | null | undefined,
): string {
  const key = `tier_${tier}` as keyof typeof PROPERTY_PHOTO_IDS;
  const arr = PROPERTY_PHOTO_IDS[key] ?? PROPERTY_PHOTO_IDS.tier_1;
  const idHash = hashString(propertyId ?? "");
  const suburbAdd = suburbCharSum(suburb ?? "");
  const idx = (idHash + suburbAdd) % arr.length;
  return arr[idx];
}

/** Builds the full Unsplash CDN URL for a photo ID. */
export function buildPropertyImageUrl(photoId: string): string {
  return `https://images.unsplash.com/${photoId}?w=800&q=80&fit=crop`;
}

/** Convenience: returns the full image URL for a property. */
export function propertyImageUrl(
  tier: PropertyTier,
  propertyId: string | null | undefined,
  suburb: string | null | undefined,
): string {
  return buildPropertyImageUrl(pickPropertyPhotoId(tier, propertyId, suburb));
}
